/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  uint32_t tTime;

  main_init();

  tTime = millis();
  while(1)
  {
    if( millis()-tTime > 100 )
    {
      tTime = millis();
      led_toggle(0);
    }

    msg_process_vcp();
  }
}

void main_init()
{
  bsp_init();
  hal_init();

  if( wdg_get_reset() == FALSE )
  {
    if( button_read(0) == FALSE )
    {
      delay_ms(10);
      if( button_read(0) == FALSE )
      {
        led_on(0);
        jump_to_fw();
      }
    }
  }
}

